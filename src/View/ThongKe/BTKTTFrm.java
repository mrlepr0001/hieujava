    private double tongTien(CAUTHU cauThu) {
        double tongTien = 0;
        for (PCVT pcvt : dsPhanCong) {
            if(pcvt.getCauThu().getMaCT() == cauThu.getMaCT()) {
                tongTien += pcvt.getSoTran() * pcvt.getViTri().getMucThuong();
            }
        }
        tongTien += cauThu.getMucLuong() * 0.1;
        return tongTien;
    }
