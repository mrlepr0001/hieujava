    private void hienThiThongKe() {
        model.setRowCount(0);
        for (CAUTHU cauThu : dsCauThu) {
            System.out.println(cauThu.getHoTen());
            Object[] row = {
                cauThu.getMaCT(),
                cauThu.getHoTen(),
                tongTien(cauThu)
            };
            model.addRow(row);
        }
    }

